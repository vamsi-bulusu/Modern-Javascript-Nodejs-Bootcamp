// An object is a collection of different types represented as a single entity

let person = {
    name: 'vamsi',
    age: '21',
    location: 'Hyderabad'
}

console.log(`My name is ${person.name} and I am ${person.age} and I am from ${person.location}`)


// We can access object properties using dot representation
console.log(person.name)